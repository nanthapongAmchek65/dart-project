import 'dart:io';

void main() {
  stdout.write("กรุณาป้อนตัวอักษรภาษาไทย: ");
  String? input = stdin.readLineSync()?.trim(); // ตัดช่องว่างรอบๆ อินพุต

  if (input == null || input.isEmpty || input.length != 1) {
    print("กรุณาป้อนตัวอักษรภาษาไทยเพียง 1 ตัว");
    return;
  }

  const vowels = [
    'ะ', 'า', 'ิ', 'ี', 'ึ', 'ื', 'ุ', 'ู', 'เ', 'แ', 'โ', 'ใ', 'ไ', 
    'ฤ', 'ฤๅ', 'ฦ', 'ฦๅ'
  ];

  const consonants = [
    'ก', 'ข', 'ฃ', 'ค', 'ฅ', 'ฆ', 'ง', 'จ', 'ฉ', 'ช', 'ซ', 'ฌ', 'ญ', 
    'ฎ', 'ฏ', 'ฐ', 'ฑ', 'ฒ', 'ณ', 'ด', 'ต', 'ถ', 'ท', 'ธ', 'น', 'บ', 
    'ป', 'ผ', 'ฝ', 'พ', 'ฟ', 'ภ', 'ม', 'ย', 'ร', 'ล', 'ว', 'ศ', 'ษ', 
    'ส', 'ห', 'ฬ', 'อ', 'ฮ'
  ];

  if (vowels.contains(input)) {
    print("ตัวอักษร '$input' เป็นสระ");
  } else if (consonants.contains(input)) {
    print("ตัวอักษร '$input' เป็นพยัญชนะ");
  } else {
    print("ตัวอักษร '$input' ไม่สามารถระบุได้ (อาจเป็นวรรณยุกต์หรืออักขระพิเศษ)");
  }
}
